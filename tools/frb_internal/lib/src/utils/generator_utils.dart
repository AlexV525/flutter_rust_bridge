import 'dart:io';

const _kPrelude = '// NOTE: This (test) file is auto-generated by frb_internal.\n'
    'Please do not modify manually, but modify the (quite straightforward) source in frb_internal.\n\n';

void writeCodeFiles(Uri baseDir, Map<String, String> textOfPathMap) {
  for (final entry in textOfPathMap.entries) {
    File(baseDir.resolve(entry.key).toFilePath()).writeAsStringSync(_kPrelude + entry.value);
  }
}
